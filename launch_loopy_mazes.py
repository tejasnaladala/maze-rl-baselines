"""MAZE TOPOLOGY AUDIT (per Codex review):

Wall-follower trivially solves tree-like mazes (the kind generated by
recursive backtracking). Codex critique: "wall-following at 100% screams
perfect/simply-connected maze artifact."

This launcher generates LOOPY mazes (multi-path topology) by adding
random extra openings to the recursive-backtracking output. With loops,
the left-hand rule can get caught in inner loops and fail.

We test all main agents on:
  - 'tree': original mazes (matches main sweep)
  - 'loopy_30': add 30% extra openings
  - 'loopy_60': add 60% extra openings

3 topologies x 5 agents x 20 seeds x 3 sizes = 900 runs

If wall-following STAYS at 100% on loopy mazes too, the prior is robust.
If wall-following DROPS, we report this honestly.
"""

